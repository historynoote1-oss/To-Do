async function main() {
  const bulkArg = process.argv.find((a) => a.startsWith('--bulk='));
  const bulkCount = bulkArg ? Number(bulkArg.split('=')[1]) : 0;

  for (const entry of seedData) {
    const exists = await prisma.update.findFirst({ where: { title: entry.title } });
    if (!exists) {
      await prisma.update.create({ data: entry });
      console.log(`✓ اتضاف: ${entry.title}`);
    } else {
      console.log(`↷ موجود أصلاً: ${entry.title}`);
    }
  }

  // وضع اختياري لتوليد عدد كبير من التحديثات الوهمية عشان اختبار الأداء
  // والتأكد إن التصفح والبحث لسه سريعين حتى مع مئات الآلاف من الصفوف.
  // مثال: npm run seed:updates -- --bulk=900000
  if (bulkCount > 0) {
    console.log(`جاري توليد ${bulkCount} تحديث تجريبي للاختبار...`);
    const BATCH = 1000;
    const base = Date.now();
    for (let start = 0; start < bulkCount; start += BATCH) {
      const size = Math.min(BATCH, bulkCount - start);
      const rows = Array.from({ length: size }, (_, i) => {
        const n = start + i;
        return {
          version: `test-${n}`,
          emoji: '🧪',
          title: `تحديث تجريبي رقم ${n}`,
          features: [`تفصيل تجريبي رقم ${n}`, 'بيانات اختبار أداء فقط'],
          howToSteps: [],
          authorName: 'سكريبت الاختبار',
          publishedAt: new Date(base - n * 1000),
        };
      });
      await prisma.update.createMany({ data: rows });
      console.log(`  ${start + size}/${bulkCount}`);
    }
  }

  await prisma.$disconnect();
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
